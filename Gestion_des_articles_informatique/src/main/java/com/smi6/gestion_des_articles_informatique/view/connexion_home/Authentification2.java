    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String login = jTextField1.getText();
    String password = new String(jPasswordField1.getPassword());

    AutentificationController controller = new AutentificationController();
    Utilisateur user = controller.authenticate(login, password);

    if (user != null) {
        if (user.getRole() == Utilisateur.Role.admin) {
            JOptionPane.showMessageDialog(this, "Bienvenue Admin " + user.getNomComplet() + " !");
            Accueille_admin2 A = new Accueille_admin2(user);
            A.setVisible(true);
            this.dispose();
        } else if (user.getRole() == Utilisateur.Role.utilisateur) {
            JOptionPane.showMessageDialog(this, "Bienvenue Utilisateur " + user.getNomComplet() + " !");
            // Open User Dashboard, pass the user if needed
            Accueille_avec_compte2 A = new Accueille_avec_compte2(user);
            A.setVisible(true);
            this.dispose();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Login ou mot de passe incorrect.", "Erreur", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

